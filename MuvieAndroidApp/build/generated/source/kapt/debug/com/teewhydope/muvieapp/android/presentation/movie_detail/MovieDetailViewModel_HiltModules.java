@OriginatingElement(
    topLevelClass = MovieDetailViewModel.class
)
public final class MovieDetailViewModel_HiltModules {
  private MovieDetailViewModel_HiltModules() {
  }

  @Module
  @InstallIn(ViewModelComponent.class)
  public abstract static class BindsModule {
    private BindsModule() {
    }

    @Binds
    @IntoMap
    @StringKey("com.teewhydope.muvieapp.android.presentation.movie_detail.MovieDetailViewModel")
    @HiltViewModelMap
    public abstract ViewModel binds(MovieDetailViewModel vm);
  }

  @Module
  @InstallIn(ActivityRetainedComponent.class)
  public static final class KeyModule {
    private KeyModule() {
    }

    @Provides
    @IntoSet
    @HiltViewModelMap.KeySet
    public static String provide() {
      return "com.teewhydope.muvieapp.android.presentation.movie_detail.MovieDetailViewModel";
    }
  }
}
